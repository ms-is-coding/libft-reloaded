#include "libft.h"

_Bool	ft_vector_some(t_vector *vec, _Bool (*fn)(size_t, t_value))
{
	size_t	i;

	i = 0;
	while (i < vec->length)
	{
		if (fn(i, vec->data[(vec->offset + i) % vec->capacity]))
			return (true);
		i++;
	}
	return (false);
}
