#include "libft.h"

t_result	ft_vector_unshift(t_vector *vec, t_value value)
{
	if (vec->length == vec->capacity
		&& ft_vector_resize(vec, vec->capacity * 1.5) != RESULT_OK)
		return (RESULT_ERROR);
	vec->length++;
	vec->offset--;
	if (vec->offset == -1UL)
		vec->offset = vec->capacity - 1;
	vec->data[vec->offset % vec->capacity] = value;
	return (RESULT_OK);
}
