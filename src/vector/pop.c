#include "libft.h"

t_value	ft_vector_pop(t_vector *vec)
{
	if (!vec->length)
		return (ft_gen_val(TYPE_UNDEFINED, (t_any){0}));
	return (vec->data[(vec->offset + vec->length-- - 1) % vec->capacity]);
}
