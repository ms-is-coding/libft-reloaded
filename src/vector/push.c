#include "libft.h"

t_result	ft_vector_push(t_vector *vec, t_value value)
{
	if (vec->length == vec->capacity
		&& ft_vector_resize(vec, vec->capacity * 1.5) != RESULT_OK)
		return (RESULT_ERROR);
	vec->data[(vec->length++ + vec->offset) % vec->capacity] = value;
	return (RESULT_OK);
}
