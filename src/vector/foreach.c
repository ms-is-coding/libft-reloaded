#include "libft.h"

void	ft_vector_foreach(t_vector *vec, void (*fn)(size_t, t_value))
{
	size_t	i;

	i = 0;
	while (i < vec->length)
	{
		fn(i, vec->data[(vec->offset + i) % vec->capacity]);
		i++;
	}
}
