#include "libft.h"

void	ft_vector_map(t_vector *vec, t_value (*fn)(size_t, t_value))
{
	size_t	i;
	t_value	*cell;

	i = 0;
	while (i < vec->length)
	{
		cell = vec->data + (vec->offset + i) % vec->capacity;
		*cell = fn(i, *cell);
		i++;
	}
}
