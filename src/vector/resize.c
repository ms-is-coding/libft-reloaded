#include "libft.h"
#include <assert.h>

t_result	ft_vector_resize(t_vector *vec, size_t size)
{
	t_value	*new_data;

	if (size <= vec->capacity)
		return (RESULT_ERROR);
	new_data = ft_malloc(size * sizeof(t_value));
	if (!new_data)
		return (RESULT_ERROR);

	return (RESULT_ERROR);
}
